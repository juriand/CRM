package com.crm.vo;

public class Dictionary {
    private String dictionary_id;
    private String dictionary_type;
    private String dictionary_entry;
    private String dictionary_value;
    private String dictionary_editable;
    
   
    
	public String getDictionary_id() {
		return dictionary_id;
	}
	public void setDictionary_id(String dictionary_id) {
		this.dictionary_id = dictionary_id;
	}
	public String getDictionary_type() {
		return dictionary_type;
	}
	public void setDictionary_type(String dictionary_type) {
		this.dictionary_type = dictionary_type;
	}
	public String getDictionary_entry() {
		return dictionary_entry;
	}
	public void setDictionary_entry(String dictionary_entry) {
		this.dictionary_entry = dictionary_entry;
	}
	public String getDictionary_value() {
		return dictionary_value;
	}
	public void setDictionary_value(String dictionary_value) {
		this.dictionary_value = dictionary_value;
	}
	public String getDictionary_editable() {
		return dictionary_editable;
	}
	public void setDictionary_editable(String dictionary_editable) {
		this.dictionary_editable = dictionary_editable;
	}
    
    
}
